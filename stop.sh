#!/bin/bash

# AI智能客服问答系统 - 停止脚本

echo "======================================"
echo "  AI智能客服问答系统 - 停止中..."
echo "======================================"

# 停止后端服务（端口 8080）
if lsof -ti:8080 > /dev/null 2>&1; then
    echo "🛑 停止后端服务（端口 8080）..."
    lsof -ti:8080 | xargs kill -9 2>/dev/null
    sleep 2
    
    # 检查是否成功停止
    if ! lsof -ti:8080 > /dev/null 2>&1; then
        echo "✅ 后端服务已停止"
    else
        echo "❌ 后端服务停止失败"
        exit 1
    fi
else
    echo "ℹ️  后端服务未运行（端口 8080）"
fi

echo ""
echo "======================================"
echo "  所有服务已停止"
echo "======================================"

